    private void ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultarActionPerformed
        if (Nombre.getText().matches("^[A-Z][a-z]{1,}")) {
            try {
                ArrayList array = DanielBarragues.Consultar(Nombre.getText(),TipoDeConsulta);
                if(TipoDeConsulta == "alumnos"){
                    Iterator it = array.iterator();
                    int x = 0;
                    while (it.hasNext()) {                  
                        Consulta c = (Consulta) array.get(x);
                        Resultados.setText(Resultados.getText() + "DNI: " + c.getDNI() + " Nombre: " + c.getNombre() + "\n");
                        it.next();
                        x++;
                    }
                }
            } catch (Exception ex) {
                Logger.getLogger(ConsultasdeProfesores.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Introduzca un nombre v√°lido");
        }
        //consultar BDD
    }//GEN-LAST:event_ConsultarActionPerformed

