    public boolean validar(){
        boolean ret=true;
        CadenaError="Los siguientes elementos estan en un formato incorrecto\n";
        error2=false;
        try{
            if(!validation(tfDni.getText(),"^.*$",ErrDni))
                ret=false;
            if(error){
                CadenaError+="-DNI\n";
                error2=true;
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Ha ocurrido un error");
        } 
        finally{
            try{
                if(!validation(tfNombre.getText(),"^.*$",ErrNombre))
                    ret=false;
                if(error){
                    CadenaError+="-Nombre\n";
                    error2=true;
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(this,"Ha ocurrido un error");
            } 
            finally{
                try{
                    if(!validation(tfAp1.getText(),"^.*$",ErrAp1))
                        ret=false;
                    if(error){
                        CadenaError+="-Primer apellido\n";
                        error2=true;
                    }
                }catch(Exception e){
                    JOptionPane.showMessageDialog(this,"Ha ocurrido un error");
                } 
                finally{
                    try{
                        if(!validation(tfAp2.getText(),"^.*$",ErrAp2))
                            ret=false;
                        if(error){
                            CadenaError+="-Segundo apellido\n";
                            error2=true;
                        }
                    }catch(Exception e){
                        JOptionPane.showMessageDialog(this,"Ha ocurrido un error");        
                    }
                        finally{
                            try{
                            if(!validation(tfDir.getText(),"^.*$",ErrDir))
                                ret=false;
                            if(error){
                                CadenaError+="-Direccion\n";
                                error2=true;
                            }
                        }catch(Exception e){
                            JOptionPane.showMessageDialog(this,"Ha ocurrido un error");        
                        }
                            finally{
                                try{
                                if(!validation(tfTlfo.getText(),"^.*$",ErrTlfo))
                                    ret=false;
                                if(error){
                                    CadenaError+="-Telefono\n";
                                    error2=true;
                                }
                            }catch(Exception e){
                                JOptionPane.showMessageDialog(this,"Ha ocurrido un error");        
                            }
                                finally{
                                    if(error2)
                                        JOptionPane.showMessageDialog(this, CadenaError);
                                }
                            }
                    }
                }   
            }
        }  
        return ret;
    }
    public boolean validation(String campo,String regex, javax.swing.JLabel err){
        try{
            if(campo.isEmpty())
                throw new DatoVacio();
            else
                err.setVisible(false);
            if(!pattern(campo,regex))
                throw new DatoNoValido();
        }catch (DatoVacio e){            
            err.setVisible(true);
            return false;
        }catch (DatoNoValido e){
            error=true;
            return false;                   
        }
        error=false;
        return true;
    }
    public boolean pattern(String dato, String regex){
        Pattern pat=Pattern.compile(regex);
        Matcher m=pat.matcher(dato);
        return m.matches();
    } 
